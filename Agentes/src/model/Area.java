package model;

public class Area {
	private int widt;
	private int length;
	
	
	public Area(int widt, int length) {
		this.widt = widt;
		this.length = length;
	}


	public int getWidt() {
		return widt;
	}


	public void setWidt(int widt) {
		this.widt = widt;
	}


	public int getLength() {
		return length;
	}


	public void setLength(int length) {
		this.length = length;
	}
	
	
	
	
	
	
}
